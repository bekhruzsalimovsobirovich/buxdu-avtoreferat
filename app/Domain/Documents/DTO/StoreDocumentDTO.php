<?php

namespace App\Domain\Documents\DTO;

use Illuminate\Http\UploadedFile;

class StoreDocumentDTO
{
    private string $subject;
    private string $type;
    private int $year;
    private int $page;
    private string $lang;
    private ?string $description;
    private ?UploadedFile $file = null;

    public static function fromArray(array $data)
    {
        $dto = new self();
        $dto->setSubject($data['subject']);
        $dto->setType($data['type']);
        $dto->setYear($data['year']);
        $dto->setPage($data['page']);
        $dto->setLang($data['lang']);
        $dto->setDescription($data['description'] ?? null);
        $dto->setFile($data['file'] ?? null);
        return $dto;
    }

    /**
     * @return string
     */
    public function getSubject(): string
    {
        return $this->subject;
    }

    /**
     * @param string $subject
     */
    public function setSubject(string $subject): void
    {
        $this->subject = $subject;
    }

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type): void
    {
        $this->type = $type;
    }

    /**
     * @return int
     */
    public function getYear(): int
    {
        return $this->year;
    }

    /**
     * @param int $year
     */
    public function setYear(int $year): void
    {
        $this->year = $year;
    }

    /**
     * @return int
     */
    public function getPage(): int
    {
        return $this->page;
    }

    /**
     * @param int $page
     */
    public function setPage(int $page): void
    {
        $this->page = $page;
    }

    /**
     * @return string
     */
    public function getLang(): string
    {
        return $this->lang;
    }

    /**
     * @param string $lang
     */
    public function setLang(string $lang): void
    {
        $this->lang = $lang;
    }

    /**
     * @return string|null
     */
    public function getDescription(): ?string
    {
        return $this->description;
    }

    /**
     * @param string|null $description
     */
    public function setDescription(?string $description): void
    {
        $this->description = $description;
    }

    /**
     * @return UploadedFile|null
     */
    public function getFile(): ?UploadedFile
    {
        return $this->file;
    }

    /**
     * @param UploadedFile|null $file
     */
    public function setFile(?UploadedFile $file): void
    {
        $this->file = $file;
    }
}
